    public int convertirATamanoBloques(int tamArchivoKB) {
    int tamBloqueKB = sistema.getDisco().getTamBloqueKB();
    return (int) Math.ceil((double) tamArchivoKB / tamBloqueKB);
}
    
public void cargarDirectoriosEnComboBox() {
    jComboBox1.removeAllItems();
    listarDirectoriosRecursivo(sistema.getRoot());
}

public void listarDirectoriosRecursivo(Directorio actual) {
    jComboBox1.addItem(actual.getNombre()); // solo el nombre

    Nodo<Directorio> nodo = actual.getSubdirectorios().getFirst();

    while (nodo != null) {
        listarDirectoriosRecursivo(nodo.getElement());
        nodo = nodo.getNext();
    }
}

public Directorio buscarDirectorioPorNombreN(Directorio actual, String nombre) {

    if (actual.getNombre().equalsIgnoreCase(nombre)) {
        return actual;
    }

    Nodo<Directorio> nodo = actual.getSubdirectorios().getFirst();

    while (nodo != null) {
        Directorio encontrado = buscarDirectorioPorNombreN(nodo.getElement(), nombre);
        if (encontrado != null) return encontrado;

        nodo = nodo.getNext();
    }

    return null;
}

    
    private void recorrerDirectorios(Directorio dir) {

    // Agregar directorio actual al ComboBox
    jComboBox1.addItem(dir.getNombre());

    // Obtener la lista de subdirectorios
    LinkedList<Directorio> lista = dir.getSubdirectorios();

    Nodo<Directorio> nodo = lista.getFirst();  // primer nodo

    // Recorrer cada nodo de la lista
    while (nodo != null) {
        Directorio sub = nodo.getElement();
        recorrerDirectorios(sub);   // recursión
        nodo = nodo.getNext();      // avanzar
    }
}


