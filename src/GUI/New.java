    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombreArchivo = jTextField1.getText();
    String tamKBStr = jTextField2.getText();
    String nombreDirectorio = (String) jComboBox1.getSelectedItem();

    if (nombreArchivo.isEmpty() || tamKBStr.isEmpty() || nombreDirectorio == null) {
        JOptionPane.showMessageDialog(this, "Complete todos los campos");
        return;
    }

    int tamKB;
    try {
        tamKB = Integer.parseInt(tamKBStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El tamaño debe ser un número");
        return;
    }

    // buscar directorio real
    Directorio destino = buscarDirectorioPorNombreN(sistema.getRoot(), nombreDirectorio);

    if (destino == null) {
        JOptionPane.showMessageDialog(this, "No se encontró el directorio seleccionado");
        return;
    }

    int bloques = convertirATamanoBloques(tamKB);

    sistema.crearProceso(
            "px",
            "createFile",
            nombreArchivo,
            bloques,
            destino,
            "admin",
            5
    );
    
    PlanificadorES planificador = new PlanificadorES(sistema);
    while (sistema.hayProcesosPendientes()) {
        planificador.ejecutarPrioridad();
    }
    if (mainUI != null) {
        mainUI.actualizarArbol();
        mainUI.llenarTablaAsignacion(sistema);
        mainUI.dibujarDisco();
    }

    // 3️⃣ Actualizar interfaz
    cargarDirectoriosEnComboBox();

    JOptionPane.showMessageDialog(this, "Proceso creado correctamente");
    }//GEN-LAST:event_jButton1ActionPerformed

