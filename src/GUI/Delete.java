    private void configurarListeners() {
    // Cuando cambie el directorio, actualizar los archivos
    jComboBox1.addActionListener(e -> {
        String nombreDir = (String) jComboBox1.getSelectedItem();
        if (nombreDir != null) {
            Directorio dir = buscarDirectorioPorNombreN(sistema.getRoot(), nombreDir);
            if (dir != null) {
                cargarArchivosEnComboBox(dir);
            }
        }
    });

    // Cuando se haga click en "Eliminar"
    jButton1.addActionListener(e -> eliminarArchivo());
}
    
    private void eliminarArchivo() {
    String nombreDirectorio = (String) jComboBox1.getSelectedItem();
    String nombreArchivo = (String) jComboBox2.getSelectedItem();

    if (nombreDirectorio == null || nombreArchivo == null) {
        JOptionPane.showMessageDialog(this, "Seleccione un directorio y un archivo");
        return;
    }

    Directorio dir = buscarDirectorioPorNombreN(sistema.getRoot(), nombreDirectorio);
    if (dir == null) {
        JOptionPane.showMessageDialog(this, "Directorio no encontrado");
        return;
    }

    // Crear proceso de eliminaci√≥n
    sistema.crearProceso(
        "px_delete",
        "delete",
        nombreArchivo,
        0,
        dir,
        "admin",
        5
    );

    PlanificadorES planificador = new PlanificadorES(sistema);
    while (sistema.hayProcesosPendientes()) {
        planificador.ejecutarPrioridad();
    }

    // Actualizar UI principal
    if (mainUI != null) {
        mainUI.actualizarArbol();
        mainUI.llenarTablaAsignacion(sistema);
        mainUI.dibujarDisco();
    }

    // Actualizar JComboBox de archivos
    cargarArchivosEnComboBox(dir);

    JOptionPane.showMessageDialog(this, "Archivo eliminado correctamente");
}

    private void cargarDirectoriosEnComboBox() {
        jComboBox1.removeAllItems();
        listarDirectoriosRecursivo(sistema.getRoot());
    }

    public void cargarArchivosEnComboBox(Directorio dir) {
    jComboBox2.removeAllItems();
    LinkedList<Archivo> archivos = dir.getArchivos();
    for (int i = 0; i < archivos.getLength(); i++) {
        Archivo a = (Archivo) archivos.getElementIn(i); // casteo obligatorio
        jComboBox2.addItem(a.getNombre());
    }
}

    private void listarDirectoriosRecursivo(Directorio actual) {
        jComboBox1.addItem(actual.getNombre());
        Nodo<Directorio> nodo = actual.getSubdirectorios().getFirst();
        while (nodo != null) {
            listarDirectoriosRecursivo(nodo.getElement());
            nodo = nodo.getNext();
        }
    }

    private Directorio buscarDirectorioPorNombreN(Directorio actual, String nombre) {
    if (actual.getNombre().equalsIgnoreCase(nombre)) return actual;
    Nodo<Directorio> nodo = actual.getSubdirectorios().getFirst();
    while (nodo != null) {
        Directorio encontrado = buscarDirectorioPorNombreN(nodo.getElement(), nombre);
        if (encontrado != null) return encontrado;
        nodo = nodo.getNext();
    }
    return null;
}

